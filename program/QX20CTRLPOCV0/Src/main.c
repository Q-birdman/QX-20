int tick = 1;

rudDegState rudder;
elvDegState elevator;

void tickPlus(){
  tick++;
}

int main(void)
{
  LL_APB2_GRP1_EnableClock(LL_APB2_GRP1_PERIPH_SYSCFG);
  LL_APB1_GRP1_EnableClock(LL_APB1_GRP1_PERIPH_PWR);

  NVIC_SetPriorityGrouping(NVIC_PRIORITYGROUP_4);
  SystemClock_Config();
  MX_GPIO_Init();
  MX_TIM3_Init();
  MX_USART1_UART_Init();

  rudder.N = RUD_N;
  elevator.N = ELV_N;
  while (1) {
    if(tick%500 == 0){
      
      if (!(GPIOA->IDR & 0x0001)) {//0000 0001 up
        elevator.N += 500;
      }
      if (!(GPIOA->IDR & 0x0002)) {//0000 0010 down
        elevator.N -= 500;
      }
      if (!(GPIOA->IDR & 0x0004)) {//0000 0100 left
        elevator.N -= 50;
      }
      if (!(GPIOA->IDR & 0x0008)) {//0000 1000 right
        elevator.N += 50;
      }
      TIM3->CCR2 = elevator.N;
      TIM3->CCR3 = rudder.N;
      
      LL_USART_TransmitData9(USART1, (char)((elevator.N/10000)+'0'));
      while (!LL_USART_IsActiveFlag_TXE(USART1));
      LL_USART_TransmitData9(USART1, (char)((elevator.N%10000/1000)+'0'));
      while (!LL_USART_IsActiveFlag_TXE(USART1));
      LL_USART_TransmitData9(USART1, (char)((elevator.N%1000/100)+'0'));
      while (!LL_USART_IsActiveFlag_TXE(USART1));
      LL_USART_TransmitData9(USART1, (char)((elevator.N%100/10)+'0'));
      while (!LL_USART_IsActiveFlag_TXE(USART1));
      LL_USART_TransmitData9(USART1, (char)((elevator.N%10)+'0'));
      while (!LL_USART_IsActiveFlag_TXE(USART1));
      LL_USART_TransmitData9(USART1, '\n');
      while (!LL_USART_IsActiveFlag_TXE(USART1));
	  }
  }
  /* USER CODE END 3 */
}

/**
  * @brief System Clock Configuration
  * @retval None
  */
void SystemClock_Config(void)
{
  LL_FLASH_SetLatency(LL_FLASH_LATENCY_1);

  if(LL_FLASH_GetLatency() != LL_FLASH_LATENCY_1)
  {
  Error_Handler();  
  }
  LL_RCC_HSI_Enable();

   /* Wait till HSI is ready */
  while(LL_RCC_HSI_IsReady() != 1)
  {
    
  }
  LL_RCC_HSI_SetCalibTrimming(16);
  LL_RCC_PLL_ConfigDomain_SYS(LL_RCC_PLLSOURCE_HSI_DIV_2, LL_RCC_PLL_MUL_9);
  LL_RCC_PLL_Enable();

   /* Wait till PLL is ready */
  while(LL_RCC_PLL_IsReady() != 1)
  {
    
  }
  LL_RCC_SetAHBPrescaler(LL_RCC_SYSCLK_DIV_1);
  LL_RCC_SetAPB1Prescaler(LL_RCC_APB1_DIV_1);
  LL_RCC_SetAPB2Prescaler(LL_RCC_APB1_DIV_1);
  LL_RCC_SetSysClkSource(LL_RCC_SYS_CLKSOURCE_PLL);

   /* Wait till System clock is ready */
  while(LL_RCC_GetSysClkSource() != LL_RCC_SYS_CLKSOURCE_STATUS_PLL)
  {
  
  }
  LL_Init1msTick(36000000);
  LL_SYSTICK_SetClkSource(LL_SYSTICK_CLKSOURCE_HCLK);
  LL_SetSystemCoreClock(36000000);
  LL_RCC_SetUSARTClockSource(LL_RCC_USART1_CLKSOURCE_PCLK1);
  LL_SYSTICK_EnableIT();
}

